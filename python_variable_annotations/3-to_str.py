#!/usr/bin/env python3
"""Ce script définit une fonction qui convertit un float en chaîne
de caractères."""


def to_str(n: float) -> str:
    """Convertit un nombre flottant en chaîne de caractères.

    n: nombre à convertir (float)
    Retourne: la représentation en chaîne de n (str)
    """
    return str(n)
