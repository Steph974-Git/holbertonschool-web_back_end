#!/usr/bin/env python3
"""Ce script définit une fonction d'addition avec annotations de type."""


def add(a: float, b: float) -> float:
    """Additionne deux nombres à virgule flottante.

    a: premier nombre (float)
    b: second nombre (float)
    Retourne: la somme de a et b (float)
    """
    return a + b
