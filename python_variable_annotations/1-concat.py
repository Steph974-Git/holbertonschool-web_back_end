#!/usr/bin/env python3
"""Ce script définit une fonction de concaténation de chaînes
avec annotations de type."""


def concat(str1: str, str2: str) -> str:
    """Concatène deux chaînes de caractères.

    str1: première chaîne (str)
    str2: seconde chaîne (str)
    Retourne: la concaténation de str1 et str2 (str)
    """
    return str1 + str2
